//praktek matematika sederhana
public class Math {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;
        int z = 5;
        int aa = y * z;

        System.out.println(x + y); //7
        System.out.println(z - x); //2
        System.out.println(aa); //20
        System.out.println(aa / y); //5
        System.out.println(aa % x); //2
    }
}